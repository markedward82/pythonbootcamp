#backslash t allow to tab in the string
tabby_cat = "\tI'm tabbed in."
#backlash n allows to jump to next page
persian_cat = "I'm splitting\non a line."
#double back slash allows to use single back slash
backslash_cat = "I'm \\a \\ cat."

#in the multiple string like this, back slash t gives tab space
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishes
\t* Catnip\n\t* Grass
"""

#display all the outcome
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
